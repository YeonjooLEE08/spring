package com.kh.library.item.vo;

import java.util.List;

public class ItemImgVO {
	private int itemImgCode;
	private String itemOriginName;
	private String itemAtImgName;
	private String isMain;
	private String itemCode;
	private List<ItemImgVO> itemImgList;
	
	public List<ItemImgVO> getItemImgList() {
		return itemImgList;
	}
	public void setItemImgList(List<ItemImgVO> itemImgList) {
		this.itemImgList = itemImgList;
	}
	public int getItemImgCode() {
		return itemImgCode;
	}
	public void setItemImgCode(int nextItemImgCode) {
		this.itemImgCode = nextItemImgCode;
	}
	public String getItemOriginName() {
		return itemOriginName;
	}
	public void setItemOriginName(String itemOriginName) {
		this.itemOriginName = itemOriginName;
	}
	public String getItemAtImgName() {
		return itemAtImgName;
	}
	public void setItemAtImgName(String itemAtImgName) {
		this.itemAtImgName = itemAtImgName;
	}
	public String getIsMain() {
		return isMain;
	}
	public void setIsMain(String isMain) {
		this.isMain = isMain;
	}
	public String getItemCode() {
		return itemCode;
	}
	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}
	
	
}
